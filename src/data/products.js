export const getByCategory = (cat) => products.filter(p => p.category === cat)
export const getBySlug = (slug) => products.find(p => p.slug === slug)
export const getFeatured = () => {
  const bats = products.filter(p => p.category === 'bats')
  const gear = products.filter(p => p.category === 'gear')
  const apparel = products.filter(p => p.category === 'apparel')

  // Pick top 2 items from each category (preferring items with badges)
  const featuredBats = [...bats.filter(p => p.badge), ...bats.filter(p => !p.badge)].slice(0, 2)
  const featuredGear = [...gear.filter(p => p.badge), ...gear.filter(p => !p.badge)].slice(0, 2)
  const featuredApparel = [...apparel.filter(p => p.badge), ...apparel.filter(p => !p.badge)].slice(0, 2)

  return [...featuredBats, ...featuredGear, ...featuredApparel]
}
